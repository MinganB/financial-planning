<h3>Introduction</h3>
<p>
    Financial planners in South Africa offer a range of services, including budgeting guidance, estate and retirement planning, 
    and wealth-building strategy. Many aspects of clients’ financial wellness could be enhanced through better collaboration 
    with their financial planner. A client-facing web application was conceptualised to facilitate improved interaction between 
    clients and financial advisers throughout the financial planning process.
</p>

<p>
    The application could significantly enhance an adviser’s client value proposition while streamlining their data capture process. 
    In selecting an appropriate development approach for the project, the key considerations were structure and predictability. 
    These considerations were most crucial given the constraints imposed by the project timeframe and the inherent resource 
    limitations of a single-developer project.
</p>

<h3>Application Scope and Features</h3>

<p>
    The application will enable users to update their personal details, create and manage a personal budget, 
    record their assets and liabilities, and view their financial planning summary information. An API 
    (Application Programming Interface) will allow third-party applications used by financial planners 
    to view and edit client data.
</p>

<p>
    Personal details, such as a client’s marital status and planned retirement age, have a major impact 
    on various aspects of their financial and estate planning. Clients should be able to update these 
    details in the application, which can then be queried by a financial planner through the API endpoints.
</p>

<p>
    The application can also be used as part of the data capturing step in financial planners’ annual 
    review processes. The planner should be able to request the creation of a personal link, which can 
    be dispatched to clients through mediums such as SMS, WhatsApp, or email. The personal link allows 
    clients to update their personal details through an easy-to-use interface.
</p>

<p>
    The budgeting portion of the app will allow users to record planned incomes and expenses and track 
    actual transactions against planned entries. The application design will facilitate simple and easy 
    capturing of transactions.
</p>

<p>
    Net worth tracking will enable users to record and maintain a list of assets and liabilities through 
    a simple interface. Net worth comparisons over time can provide useful and motivational information 
    to clients.
</p>

